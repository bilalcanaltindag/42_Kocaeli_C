#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

#include <stdio.h>

int    main()
{
    char *c = "Hello";
    ft_putstr(c);
}
